#for,while

print("For loop operations")
#Start value, end value
#To print numbers 1 to 10
for i in range(0,10):
    print(i)
print("-----------")

#To print even numbers between 1 to 20
for i in range(2,20,2):
    print(i)
print("-----------")

#Tp print odd numbers between 1 to 8
for i in range(1,8,2):
    print(i)
print("-----------")

#To print numbers in decrement order
for i in  range (10,1,-1):
    print(i)
print("-----------")

print("While loop")
#Start value, conditions, increment/decrement

